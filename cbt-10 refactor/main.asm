	loop:
	nop
	rjmp loop

	unpack_zeroes:
		cpi arg, 0
		breq no_load
		lpm arg, Z+
		cpi arg, 0
		brne transmit
		lpm TXZCount,Z+
		rjmp transmit

		no_load:
		dec TXZCount
		brne transmit 
		lpm arg,Z+
		transmit:
	ret


	; Pepare SPI data
	SPI_start:
		clr TXZCount
		lpm TXCount,Z+
		lpm TXRowCount,Z+
		SPI_start_defined: ; if TXCount,TXRowCount preloaded possible reduce size
		lpm arg,Z+
		;rcall unpack_zeroes
		cp TXRowCount,zeroreg
		breq SPI_TX
		;IF not save parameters to memory
		sts TXCountMem, TXCount
		sts TXRowCountMem, TXRowCount
		
	SPI_rstart:
		dec TXRowCount
		cp TXRowCount,zeroreg
		breq SPI_TX
		rcall SPI_TX
		inc TXYpos
		rcall LCD_goto_XY
		sbi PORTB,P_MOSI
		
		;if arg != 0
		;cpi arg,0
		;breq zero_arg
		lpm arg,Z+
		zero_arg:
		;rcall unpack_zeroes
		;-------------
		
		lds TXCount, TXCountMem
		rjmp SPI_rstart
		
	SPI_TX:

	out SPCR, zeroreg		;disable hardware SPI
	sbi PORTB, P_SCK		;pull up SCK to send D/C SPI signal
	out SPCR, spenreg		;enable hardware SPI
	out SPDR,arg			;starting transfer
	cbi PORTB, P_SCK		;release SCK, after start to reduce cpu cycles

	;we can prepare new data here
	dec TXCount
	breq wait_TX_complete
	;=========================
	lpm arg,Z+
	
	wait_TX_complete:
	in tmpreg, SPSR
	sbrs tmpreg,SPIF
	rjmp wait_TX_complete
	cp TXCount, zeroreg
	brne SPI_TX
	ret


	;=================

	;=================
	LCD_goto_XY:
	;.def TXXpos = r10
	;.def TXYpos = r11

	;=== set 
	;=== low 4 bits
	;load position
	push arg
	push TXRowCount
	clr TXRowCount
	cbi PORTB,P_MOSI
	mov arg, TXXpos
	andi arg, 0x0F
	inc TXCount
	rcall SPI_TX
	;=== high 3 bits
	mov arg, TXXpos
	swap arg
	andi arg, 0x0F
	ori arg,0x10
	inc TXCount
	rcall SPI_TX

	;=== set 
	mov arg,TXYpos //load position
	ori arg,0xB0 ; y2..0 (0..7)
	inc TXCount
	rcall SPI_TX
	pop TXRowCount
	pop arg
	ret



