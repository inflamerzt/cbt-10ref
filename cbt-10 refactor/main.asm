	cbi		PORTD,DDD7			; к земле RES
	rcall	pause_10ms ;100ms
	;rcall	pause_100ms ;uncomment if 10ms is unstable
	sbi		PORTD,DDD7			; подтяжка RES
	rcall	pause_10ms	//delay 2500ns
	.endif


	LCD_cmd LCD_init
	
	;== clear screen loop 9 lines
	ldi tmpregh,2
	ldi tmpreg, 96*2
	sbi PORTB,P_MOSI
	clr_scr:
	push tmpreg

	LCD_rep LCD_sp

	pop tmpreg
	dec tmpreg
	brne clr_scr
	dec tmpregh
	brne clr_scr

	LCD_XY 0,0
